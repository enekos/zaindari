#[cfg(test)]
mod tests {
    use super::*;
    use crate::report::{Finding, Metric, MetricDirection, PillarResult, PillarStatus, Severity};

    fn sample_report() -> ZaindariReport {
        let mut r = ZaindariReport::empty();
        let mut gate = PillarResult::new(PillarStatus::Pass, "Eval gate held: critical F1 0.90.");
        gate.metrics.push(Metric::with_baseline(
            "council.critical_f1",
            0.90,
            0.857,
            MetricDirection::HigherBetter,
        ));
        r.pillars.gate = Some(gate);

        let mut guard = PillarResult::new(PillarStatus::Fail, "2 guard rule case(s) failed.");
        guard.findings.push(
            Finding::new(
                Severity::Major,
                "rule case failed: promo-x",
                "triggers: expected disclaimer",
            )
            .with_location("promo-x"),
        );
        r.pillars.guard = Some(guard);

        let mut watch = PillarResult::new(PillarStatus::Warn, "1 of 3 items look anomalous.");
        watch.findings.push(Finding::new(
            Severity::Major,
            "anomalous skill: Klingon",
            "cardinality 0.94 — no peers nearby",
        ));
        r.pillars.watch = Some(watch);
        r
    }

    #[test]
    fn html_is_self_contained_with_no_external_refs() {
        let html = to_html(&sample_report());
        assert!(html.contains("<!DOCTYPE html>"));
        assert!(html.contains("<style>"));
        // No external assets / scripts.
        assert!(!html.contains("<script"));
        assert!(!html.contains("http://"));
        assert!(!html.contains("https://"));
        assert!(!html.contains("cdn"));
    }

    #[test]
    fn html_renders_traffic_lights_and_headlines() {
        let html = to_html(&sample_report());
        assert!(html.contains(">Gate</h2>"));
        assert!(html.contains(">Guard</h2>"));
        assert!(html.contains(">Watch</h2>"));
        assert!(html.contains("PASS"));
        assert!(html.contains("FAIL"));
        assert!(html.contains("WARN"));
        assert!(html.contains("Eval gate held"));
    }

    #[test]
    fn html_shows_baseline_delta_and_findings() {
        let html = to_html(&sample_report());
        assert!(html.contains("council.critical_f1"));
        // delta +0.043 rendered.
        assert!(html.contains("+0.043"));
        assert!(html.contains("promo-x"));
        assert!(html.contains("Klingon"));
    }

    #[test]
    fn html_escapes_text() {
        let mut r = ZaindariReport::empty();
        r.pillars.gate = Some(PillarResult::new(PillarStatus::Pass, "a <b> & \"c\""));
        let html = to_html(&r);
        assert!(html.contains("a &lt;b&gt; &amp; &quot;c&quot;"));
    }

    #[test]
    fn text_summary_lists_each_pillar() {
        let txt = to_text(&sample_report());
        assert!(txt.contains("gate"));
        assert!(txt.contains("guard"));
        assert!(txt.contains("watch"));
        assert!(txt.contains("FAIL"));
    }
}
