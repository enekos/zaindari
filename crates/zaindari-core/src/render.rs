//! Rendering — turn a [`ZaindariReport`] into a self-contained HTML report a
//! non-engineer can read, plus a terse terminal summary.
//!
//! The HTML is one file: inline CSS, no JS, no CDN, no external assets. Plain
//! and credible — a traffic light per pillar, a one-sentence headline, metric
//! tables with baseline deltas, and findings grouped by severity.

use crate::report::{
    Finding, Metric, MetricDirection, PillarResult, PillarStatus, Severity, ZaindariReport,
};
use std::fmt::Write as _;

/// Render the full self-contained HTML report.
pub fn to_html(report: &ZaindariReport) -> String {
    let mut s = String::with_capacity(8 * 1024);
    s.push_str("<!DOCTYPE html>\n<html lang=\"en\"><head><meta charset=\"utf-8\">");
    s.push_str("<meta name=\"viewport\" content=\"width=device-width, initial-scale=1\">");
    s.push_str("<title>zaindari report</title>\n<style>\n");
    s.push_str(CSS);
    s.push_str("</style></head><body>\n");
    s.push_str("<header><h1>zaindari report</h1>");
    let _ = write!(
        s,
        "<p class=\"sub\">LLM-trust check across Gate, Guard and Watch. Schema v{}.</p>",
        report.schema_version
    );
    s.push_str("</header>\n<main>\n");

    render_pillar_card(
        &mut s,
        "Gate",
        "Pre-ship eval quality",
        report.pillars.gate.as_ref(),
    );
    render_pillar_card(
        &mut s,
        "Guard",
        "Runtime rule enforcement",
        report.pillars.guard.as_ref(),
    );
    render_pillar_card(
        &mut s,
        "Watch",
        "Production drift detection",
        report.pillars.watch.as_ref(),
    );

    s.push_str("</main>\n<footer><p>Generated by zaindari — orchestrator over aatxe, iratxo, cardinal-map.</p></footer>\n");
    s.push_str("</body></html>\n");
    s
}

fn render_pillar_card(s: &mut String, name: &str, blurb: &str, pillar: Option<&PillarResult>) {
    let (status, headline) = match pillar {
        Some(p) => (p.status, p.headline.as_str()),
        None => (PillarStatus::Skipped, "Not configured."),
    };
    let cls = status_class(status);
    let _ = write!(
        s,
        "<section class=\"card {cls}\">\n<div class=\"card-head\">"
    );
    let _ = write!(
        s,
        "<span class=\"dot {cls}\"></span><h2>{}</h2><span class=\"badge {cls}\">{}</span>",
        esc(name),
        status.label()
    );
    let _ = write!(s, "</div>\n<p class=\"blurb\">{}</p>", esc(blurb));
    let _ = writeln!(s, "<p class=\"headline\">{}</p>", esc(headline));

    if let Some(p) = pillar {
        if !p.metrics.is_empty() {
            render_metric_table(s, &p.metrics);
        }
        if !p.findings.is_empty() {
            render_findings(s, &p.findings);
        }
        if let Some(raw) = &p.raw_ref {
            let _ = write!(
                s,
                "<p class=\"raw\">raw engine output: <code>{}</code></p>",
                esc(raw)
            );
        }
    }
    s.push_str("</section>\n");
}

fn render_metric_table(s: &mut String, metrics: &[Metric]) {
    s.push_str("<table class=\"metrics\"><thead><tr><th>metric</th><th>value</th><th>baseline</th><th>change</th></tr></thead><tbody>\n");
    for m in metrics {
        let _ = write!(
            s,
            "<tr><td>{}</td><td>{}</td>",
            esc(&m.name),
            fmt_num(m.value)
        );
        match m.baseline {
            Some(b) => {
                let _ = write!(s, "<td>{}</td>", fmt_num(b));
            }
            None => s.push_str("<td class=\"muted\">—</td>"),
        }
        match m.delta {
            Some(d) => {
                let cls = delta_class(m);
                let _ = write!(s, "<td class=\"{cls}\">{}</td>", fmt_delta(d));
            }
            None => s.push_str("<td class=\"muted\">—</td>"),
        }
        s.push_str("</tr>\n");
    }
    s.push_str("</tbody></table>\n");
}

fn render_findings(s: &mut String, findings: &[Finding]) {
    s.push_str("<div class=\"findings\">\n");
    for sev in Severity::all_worst_first() {
        let group: Vec<&Finding> = findings.iter().filter(|f| f.severity == sev).collect();
        if group.is_empty() {
            continue;
        }
        let cls = severity_class(sev);
        let _ = write!(
            s,
            "<h3 class=\"sev {cls}\">{} ({})</h3>\n<ul>",
            sev.label(),
            group.len()
        );
        for f in group {
            let _ = write!(s, "<li><strong>{}</strong>", esc(&f.title));
            if let Some(loc) = &f.location {
                let _ = write!(s, " <code>{}</code>", esc(loc));
            }
            let _ = writeln!(
                s,
                "<br><span class=\"detail\">{}</span></li>",
                esc(&f.detail)
            );
        }
        s.push_str("</ul>\n");
    }
    s.push_str("</div>\n");
}

fn status_class(s: PillarStatus) -> &'static str {
    match s {
        PillarStatus::Pass => "pass",
        PillarStatus::Fail => "fail",
        PillarStatus::Warn => "warn",
        PillarStatus::Skipped => "skipped",
        PillarStatus::EngineMissing => "missing",
    }
}

fn severity_class(s: Severity) -> &'static str {
    match s {
        Severity::Critical => "fail",
        Severity::Major => "warn",
        Severity::Minor | Severity::Nit | Severity::Info => "skipped",
    }
}

fn delta_class(m: &Metric) -> &'static str {
    if m.regressed() {
        return "fail";
    }
    match (m.delta, m.direction) {
        (Some(d), MetricDirection::HigherBetter) if d > 0.0 => "pass",
        (Some(d), MetricDirection::LowerBetter) if d < 0.0 => "pass",
        _ => "muted",
    }
}

fn fmt_num(v: f64) -> String {
    if v.fract() == 0.0 && v.abs() < 1e15 {
        format!("{}", v as i64)
    } else {
        format!("{v:.3}")
    }
}

fn fmt_delta(d: f64) -> String {
    let n = if d.fract() == 0.0 && d.abs() < 1e15 {
        format!("{}", d as i64)
    } else {
        format!("{d:.3}")
    };
    if d > 0.0 {
        format!("+{n}")
    } else {
        n
    }
}

/// Minimal HTML escape for text nodes / attribute values.
fn esc(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for c in s.chars() {
        match c {
            '&' => out.push_str("&amp;"),
            '<' => out.push_str("&lt;"),
            '>' => out.push_str("&gt;"),
            '"' => out.push_str("&quot;"),
            '\'' => out.push_str("&#39;"),
            _ => out.push(c),
        }
    }
    out
}

/// A terse multi-line terminal summary (one block per pillar).
pub fn to_text(report: &ZaindariReport) -> String {
    let mut s = String::new();
    let _ = writeln!(s, "zaindari report (schema v{})", report.schema_version);
    render_text_pillar(&mut s, "gate", report.pillars.gate.as_ref());
    render_text_pillar(&mut s, "guard", report.pillars.guard.as_ref());
    render_text_pillar(&mut s, "watch", report.pillars.watch.as_ref());
    s
}

fn render_text_pillar(s: &mut String, name: &str, pillar: Option<&PillarResult>) {
    match pillar {
        None => {
            let _ = writeln!(s, "  {name:<6} ·  -  not configured");
        }
        Some(p) => {
            let _ = writeln!(s, "  {name:<6} · {:<14} {}", p.status.label(), p.headline);
            for f in &p.findings {
                let loc = f
                    .location
                    .as_deref()
                    .map(|l| format!(" [{l}]"))
                    .unwrap_or_default();
                let _ = writeln!(s, "           - {}: {}{}", f.severity.label(), f.title, loc);
            }
        }
    }
}

